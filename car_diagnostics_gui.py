    def _get_protocol_number(self):
        """Перевод названия протокола в номер ELM327 (AT SP N)."""
        mapping = {
            "AUTO":                  None,
            "ISO 9141-2 (до 2008)":  "3",
            "KWP2000":               "5",
            "CAN 11bit":             "6",
            "CAN 29bit":             "7",
        }
        return mapping.get(self.proto_var.get(), None)

