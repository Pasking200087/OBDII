def _decode_vin(vin: str | None) -> dict:
    """Декодирует VIN → dict с make, year, vin."""
    if not vin or len(vin) < 11:
        return {}
    vin = vin.upper().strip()
    wmi  = vin[:3]
    make = _WMI.get(wmi) or _WMI.get(vin[:2])
    year = _YEAR.get(vin[9])
    return {"vin": vin, "make": make or wmi, "year": year}


