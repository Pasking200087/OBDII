def _dtc_severity(code: str) -> str:
    """Определяет серьёзность кода ошибки по его категории."""
    if not code or len(code) < 2:
        return "⚠️ Средняя"
    cat = code[:2].upper()
    p3  = code[:3].upper()
    # Критические — немедленное внимание
    if cat == "B0":                          return "🔴 Высокая"   # подушки безопасности
    if p3 in ("P02", "P03"):                return "🔴 Высокая"   # форсунки, пропуски, зажигание
    if code in ("P0217", "P0219", "P0524"): return "🔴 Высокая"   # перегрев, давление масла
    if p3 in ("C00", "C01", "C02", "C03"): return "🔴 Высокая"   # ABS/ESP
    if p3 == "P07":                          return "🔴 Высокая"   # трансмиссия
    if p3 == "P06" and code[3].isdigit() and int(code[3]) < 5:
        return "🔴 Высокая"                                         # ЭБУ (P0600-P0604)
    # Низкая — влияния на безопасность нет, только диагностика/экология
    if cat == "U0":                          return "🟡 Низкая"    # шина CAN/связь
    if cat in ("U1", "U2"):                  return "🟡 Низкая"    # сеть (произв.)
    if code in ("P0420", "P0421", "P0430", "P0431",
                "P0440", "P0441", "P0442", "P0456"):
        return "🟡 Низкая"                                          # катализатор/EVAP
    if p3 == "B1":                           return "🟡 Низкая"    # кузов/комфорт
    # Средняя — всё остальное
    return "⚠️ Средняя"


